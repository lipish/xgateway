// Helper function to get translation
export function t(key: string, lang?: Language): string {
  const currentLang = lang || useI18n.getState().language
  const dict = translations[currentLang]
  
  const keys = key.split('.')
  let value: any = dict
  
  for (const k of keys) {
    value = value?.[k]
  }
  
  return value || key
}